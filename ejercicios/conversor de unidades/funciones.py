def km_a_milla(km):
    return km / 1.609

def milla_a_km(milla):
    return milla * 1.609

def celsius_a_fahrenheit(c):
    resultado = (c * 9/5) + 32
    return resultado

def fahrenheit_a_celsius(f):
    resultado = (f - 32) * 5/9
    return resultado

def kg_a_libras(kg):
    return kg * 2.205

def libras_a_kg(libras):
    return libras / 2.205
